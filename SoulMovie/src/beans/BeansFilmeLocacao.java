package beans;

public class BeansFilmeLocacao {
	private int codigo;
	private int codigo_locacao;
	private int codigo_titulo;

	
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	
	
	public int getCodigo_locacao() {
		return codigo_locacao;
	}
	public void setCodigo_locacao(int codigo_locacao) {
		this.codigo_locacao = codigo_locacao;
	}
	
	
	public int getCodigo_titulo() {
		return codigo_titulo;
	}
	public void setCodigo_titulo(int codigo_titulo) {
		this.codigo_titulo = codigo_titulo;
	}


}
